function fillStep(gate: [number, number], rooms: number[][]) {
  console.log("gate:", gate);
  let queue = [gate];
  const WALL = -1;
  const GATE = 0;

  const dimensions = [
    [-1, 0],
    [0, -1],
    [1, 0],
    [0, 1],
  ];
  let level = 0;

  const visited = new Set();
  while (queue.length > 0) {
    level += 1;
    const newQueue: [number, number][] = [];
    for (const curr of queue) {
      if (visited.has(curr[0] + "+" + curr[1])) continue;

      visited.add(curr[0] + "+" + curr[1]);
      for (const [row, col] of dimensions) {
        const newR = row + curr[0];
        const newC = col + curr[1];

        if (
          newR !== -1 &&
          newC !== -1 &&
          newR < rooms.length &&
          newC < rooms[0].length &&
          rooms[newR][newC] !== WALL &&
          rooms[newR][newC] !== GATE
        ) {
          rooms[newR][newC] = Math.min(level, rooms[newR][newC]);
          newQueue.push([newR, newC]);
        }
      }
    }

    queue = newQueue;
  }
}

function wallsAndGates(rooms: number[][]): void {
  const GATE = 0;
  for (let i = 0; i < rooms.length; i += 1) {
    for (let j = 0; j < rooms[i].length; j += 1) {
      if (rooms[i][j] === GATE) fillStep([i, j], rooms);
    }
  }

}

